    private ArrayList getEnglishWord(){
        ArrayList getArrayList = new ArrayList();
           
    for(int i = 0 ; i < Dictionary.listWord.size(); i++){
        getArrayList.add(Dictionary.listWord.get(i).EnglishWord);
    }
        Collections.sort(getArrayList);
        return getArrayList;
    }
    
    
   
   //nạp dữ liệu vào list hiển thị mặc định
   private void bindData(){
       
       getEnglishWord().stream().forEach(x ->{
            defautListModel.addElement(x);
       });
       jList1.setModel(defautListModel);
       jList1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
   
   
   }
   //tìm từ gần thấy nhất
   private void searchFilter(String searchItem){
   
            DefaultListModel filterListModel = new DefaultListModel();
       
            ArrayList stars = getEnglishWord();
       
            for(int i = 0 ; i < stars.size(); i++){
            
                String starName = stars.get(i).toString().toLowerCase();
                if(starName.contains(searchItem.toLowerCase())){
                
                    filterListModel.addElement(stars.get(i));
                
                }
        
            
            }
           
           defautListModel = filterListModel;
           jList1.setModel(defautListModel);
              
       
   
   
   }
  
