    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean check = true;
        if(jTextArea3.getText() != "")
        {
            jTextArea3.setText("");
        } 
        
            for(Word e : Dictionary.listWord){
        
                if(e.EnglishWord.equals(JText1.getText().toString().toLowerCase()))
                    {
                    jTextArea3.append(e.kindofWord+"\n"+e.transcription+ "\n" +"Nghĩa: "+e.Explain +"\n"+"VD: "+e.Example + "\n" + "Nghĩa: " + e.explainExample);
                    check = false;
                    }
            }
        if(check){
        
            JOptionPane.showMessageDialog(rootPane, "Not Found in Dictionary");
        
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed

